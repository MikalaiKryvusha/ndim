/**
 * Техническая причина сбоя — для РАЗРАБОТЧИКА, а не для человека.
 *
 * ЗАЧЕМ ЭТО ПОЯВИЛОСЬ. На боевом проде экраны выводили сырое сообщение ошибки прямо под
 * человеческой фразой. Владелец увидел у себя в профиле буквально это:
 *
 *     Не удалось загрузить данные. Обновите страницу…
 *     Cannot read properties of undefined (reading 'ru')      ← отладочный вывод живому человеку
 *
 * Человеку такая строка не говорит ничего — она только пугает и выглядит как сломанная машина.
 * А ещё через неё наружу протекает внутренняя кухня (имена коллекций, наши команды: одно из
 * сообщений прямо советовало «npm run stand»).
 *
 * Правило: **в бою человек видит человеческую фразу, а причина уходит в консоль.** На стенде
 * причина остаётся и на экране — там её читает разработчик, и она экономит ему время.
 *
 * Канон проекта: ноль внутреннего жаргона на лице приложения (`AGENT_GUIDE.md` → «Словарь»).
 * Соблюдение проверяется машиной — `e2e/no-jargon.spec.ts`.
 */
import { isStand } from '../firebase.ts';

/**
 * Возвращает техническую причину для показа на экране: на стенде — текст ошибки, в бою — пусто.
 * В консоль пишет всегда: диагностику мы не теряем, мы лишь не показываем её людям.
 */
export function technicalDetail(error: unknown): string {
  console.error(error);
  if (!isStand()) return '';
  return error instanceof Error ? error.message : String(error);
}
